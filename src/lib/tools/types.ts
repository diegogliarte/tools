import type { Component } from 'svelte';

export interface ToolDefinition {
	title: string;
	description: string;
	component: Component;
	href?: string; // autogenerated
	removeBorder?: boolean;
	fullscreen?: boolean;
	favicon?: string; // inherit from category if not set
	categoryPath?: string[]; // autogenerated, e.g. ['Development', 'Hashing']
}

export interface ToolCategory {
	name: string;
	tools: ToolDefinition[];
	subgroups: ToolCategory[];
	favicon?: string;
}
