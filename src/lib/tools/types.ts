import type { Component } from 'svelte';

export type ToolComponentProps = {
	cookieKey: string;
	cookieState: unknown;
};

export interface ToolDefinition {
	title: string;
	description: string;
	href?: string; // autogenerated
	fullscreen?: boolean;
	favicon?: string; // inherits from category if not set
	categoryPath?: string[]; // autogenerated
	order?: number;
	loadComponent: () => Promise<{
		default: Component;
	}>;
}

export interface ToolCategoryMetadata {
	title: string;
	favicon?: string;
	order?: number;
}

export interface ToolCategory {
	name: string;
	slug?: string; // autogenerated from folder name
	tools: ToolDefinition[];
	subgroups: ToolCategory[];
	favicon?: string;
	order?: number;
}